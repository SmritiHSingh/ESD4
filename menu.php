<h2>Microelectronics</h2>
Microelectronics is a subfield of electronics. As the name suggests, microelectronics relates to the study and manufacture (or microfabrication) of very small electronic designs and components. Usually, but not always, this means micrometre-scale or smaller.
<br>
These devices are typically made from semiconductor materials. Many components of normal electronic design are available in a microelectronic equivalent. These include transistors, capacitors, inductors, resistors, diodes and (naturally) insulators and conductors can all be found in microelectronic devices.
<br>
Unique wiring techniques such as wire bonding are also often used in microelectronics because of the unusually small size of the components, leads and pads. This technique requires specialized equipment and is expensive
<br>
As techniques have improved, the scale of microelectronic components has continued to decrease[citation needed]. At smaller scales, the relative impact of intrinsic circuit properties such as interconnections may become more significant.
